    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String ci_a = txtCi.getText();
        String nombre = txtNombre.getText();
        String apellido = txtApellido.getText();
        String celular = txtCelular.getText();
        if("".equals(ci_a) || ci_a==null){
            JOptionPane.showMessageDialog(null, "Error:\nEscriba CI del Arbitro");
        }else if("".equals(nombre) || nombre == null){
            JOptionPane.showMessageDialog(null, "Error:\nEscriba Nombre del Arbitro");
        }else if("".equals(apellido) || apellido == null){
            JOptionPane.showMessageDialog(null, "Error:\nEscriba Apellido del Arbitro");
        }else if("".equals(celular) || celular == null){
            JOptionPane.showMessageDialog(null, "Error:\nEscriba Celular del Arbitro");
        }else{
            //Guarda los datos en la base de datos
            cn.getConexion();
            cn.Inseratar("INSERT INTO ARBITRO (CI_A,NOMBRE,APELLIDO,TELEFONO) values('"+ci_a+"','"+nombre+"','"+apellido+"','"+celular+"')");
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

