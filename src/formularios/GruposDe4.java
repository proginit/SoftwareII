    private String extraerNombre(String nombre){
        //0000016    - carlos andrade - 16
        int n1 =nombre.indexOf('-',0)+2;
        int n2 = nombre.indexOf('-',n1+1);
        //System.out.println(nombre.substring(n1,n2));
        return nombre.substring(0,n2);
    }
    
