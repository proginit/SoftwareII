    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String Ci = txtCi.getText();
        String nombre = txtNombre.getText();
        String apellido = txtApellido.getText();
        String telefono = txtTelefono.getText();
        String FechaNac = ftxtFechaNac.getText();
        String ranking = txtRanking.getText();
        String categoria = txtCategoria.getText();
        if("".equals(Ci) || Ci == null){
            JOptionPane.showMessageDialog(null,"Error :\nEscriba Ci del Jugador");
        }else if("".equals(nombre) || nombre == null){
            JOptionPane.showMessageDialog(null,"Error :\nEscriba Nombre del Jugador");
        }else if ("".equals(apellido) || apellido == null){
            JOptionPane.showMessageDialog(null,"Error :\nEscriba Apellido del Jugador");
        }else if("".equals(telefono) || telefono == null){
            JOptionPane.showMessageDialog(null,"Error :\nEscriba Telefono del Jugador");
        }else if("  /  /    ".equals(FechaNac) || FechaNac == null){
            JOptionPane.showMessageDialog(null,"Error :\nEscriba Fecha de Nacimiento del Jugador");
        }else if("".equals(ranking) || ranking == null){
            JOptionPane.showMessageDialog(null,"Error :\nEscriba Ranking del Jugador");
        }else if("".equals(categoria) || categoria == null){
            JOptionPane.showMessageDialog(null,"Error :\nEscriba Categoria del Jugador");
        }else {
            //Guarda lod datos en la base de datos
            cn.getConexion();
            cn.Inseratar("INSERT INTO JUGADOR VALUES('"+Ci+"','"+nombre+"','"+apellido+"','"+telefono+"','"+FechaNac+"',"+Integer.parseInt(ranking)+",'"+categoria+"')");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

