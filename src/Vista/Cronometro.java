    private DecimalFormat timeFormatter;

    private Timer timer;
    
    public Cronometro() {
        initComponents();
        timeFormatter = new DecimalFormat("00");
        timer = new Timer(10, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                //throw new UnsupportedOperationException("Not supported yet."); //To change body of generated methods, choose Tools | Templates.
                if (centiseconds > 0) {
                    centiseconds--;
                } else {
                    if (seconds == 0 && minutes == 0) {
                        timer.stop();
                    } else if (seconds > 0) {
                        seconds--;
                        centiseconds = 99;
                    } else if (minutes > 0) {
                        minutes--;
                        seconds = 59;
                        centiseconds = 99;
                    }
                }
                lblCronometro.setText(timeFormatter.format(minutes)+":"+timeFormatter.format(seconds)+":"+timeFormatter.format(centiseconds));
            }
        });
    }

